# Write a program for assignment operators

num = int(input("Enter a number: "))

num += 5
print("After += 5:", num)
num -= 3
print("After -= 3:", num)
num *= 2
print("After *= 2:", num)
num /= 4
print("After /= 4:", num)
num %= 3
print("After %= 3:", num)
num //= 2
print("After //= 2:", num)
num **= 3
print("After **= 3:", num)
